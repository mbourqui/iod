function [f, g, h] = f(x)
    f = (x(1) - 2)^4 + ((x(1) - 2)^2) * x(2)^2 + (x(2) + 1)^2;
    g = [4 * ((x(1) - 2)^3) + 2 * (x(2)^2) * (x(1) - 2); 2 * ((x(1) - 2)^2) * x(2) + 2 * (x(2) + 1)];
    h(1,1) = 12 * (x(1)-2)^2 + 2 * (x(2))^2;
    h(1,2) = 4 * (x(1) - 2) * x(2);
    h(2,1) = h(1,2);
    h(2,2) = 2 * (x(1) - 2)^2 + 2;
end